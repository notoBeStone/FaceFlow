import Foundation

public enum TemperatureUnit: Int, CaseIterable {
    /**
     * 摄氏度
     * Celsius, °C
     */
    case celsius = 0
    
    /**
     * 华氏度
     * Fahrenheit, °F
     */
    case fahrenheit = 1
    
}
